package Data_operation;

public class StringApp {

	public static void main(String[] args) {
		
		System.out.println("Hello world"); // String-character들이 모여있는것
		System.out.println('H');//Character문자
		System.out.println("H");//문자열
		
		System.out.println("Hello "
				+ "world"); //줄바꿈안됨
		System.out.println("Hello \n world");//\n=>new line
		System.out.println("Hello \"world\"");//Hello "world"=>escape

	}

}
